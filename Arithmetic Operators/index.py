# Addition [+]

print(10 + 30)
print(-10 + 20)
print(1 + 2.66)
print(1.2 + 1.2)

# Subtraction 

print(60 - 30)   
print(-30 - 20)   
print(-30 - -20)  
print(5.66 - 3.44)

# Multiplication

print(10 * 3) 
print(5 + 10 * 100) 
print((5 + 10) * 100)

# Division 

print(100 / 21) # 4.761904761904762

# Exponent 

print(2 ** 5) # 32
print(2 * 2 * 2 * 2 * 2) # 32
print(5 * 5 * 5 * 5) # 625
print(5 ** 4) # 625

# Modulus 

print(8 % 2) # 0
print(9 % 2) # 1
