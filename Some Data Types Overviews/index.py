print("My name is Atif.") # string | My name is Atif.
print('My name is Atif.') # string | My name is Atif.

print(type("My name is Atif.")) # <class 'str'>
print(type('My name is Atif.')) # <class 'str'>
print(type("24")) # <class 'str'>

print(24) # 24
print(type(24)) # <class 'int'>

print(24.0) # 24.0
print(type(24.0)) # <class 'float'>

print(["Atif", "Mohamed", "Mazen", "Ibrahim", "Ahmed"]) # ["Atif", "Mohamed", "Mazen", "Ibrahim", "Ahmed"]
print(type(["Atif", "Mohamed", "Mazen", "Ibrahim", "Ahmed"])) # <class 'list'>

print(("Atif", "Mohamed", "Ibrahim")) # ('Atif', 'Mohamed', 'Ibrahim')
print(type(("Atif", "Mohamed", "Ibrahim"))) # <class 'tuple'>

print({"Name": "Atif", "Age": 24, "Country": "Egypt"}) # {'Name': 'Atif', 'Age': 24, 'Country': 'Egypt'}
print(type({"Name": "Atif", "Age": 24, "Country": "Egypt"})) # <class 'dict'>

print(True) # True
print(False) # False

print(type(True)) # <class 'bool'>
print(type(False)) # <class 'bool'>

print(2 == 2) # True
print(type(2 == 2)) # <class 'bool'>